 else if ( $meta['deal_per_night']['0'] == 1 ) {
 	$data = " / Per Night";
 }

 else if ( $meta['deal_per_person']['0'] == 1 ) {
 	$data = " / Per Person";
 }

  else  {
 	$data = "";
 }

